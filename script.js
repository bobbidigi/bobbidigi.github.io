function openNav() {
	document.getElementById('sidenav').style.width = "100%";

	//Hides menu icon 
	document.getElementById('opn').style.display = "none";
}

function closeNav() {
	document.getElementById('sidenav').style.width = "0px";

	//Unhides menu icon
	document.getElementById('opn').style.display = "block";
}
